SELECT العملاء.العميل, المستندات.[مسلسل المستند ID], المستندات.[رقم الدعوى], المستندات.[بيان المستند], المستندات.[تاريخ المستند], المستندات.[عدد الأوراق], المستندات.[تاريخ الإيداع], المستندات.[المحامي/الموظف المسئول], المستندات.ملاحظات, العملاء.[ID_client]
FROM العملاء INNER JOIN المستندات ON العملاء.العميل = المستندات.العميل
WHERE (((العملاء.العميل)=Forms![العملاء_رئيسي]!العميل));
