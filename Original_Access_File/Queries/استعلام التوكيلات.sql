SELECT التوكيلات.العميل, التوكيلات.مسلسل, التوكيلات.[اسم الموكل], التوكيلات.[صفة الموكل بالتوكيل], التوكيلات.[رقم التوكيل], التوكيلات.حرف, التوكيلات.السنة, التوكيلات.[جهة الإصدار], التوكيلات.[تاريخ الإصدار], التوكيلات.[المحامون الصادر لهم التوكيل], التوكيلات.[عدد النسخ], التوكيلات.ملاحظات, العملاء.[ID_client]
FROM العملاء INNER JOIN التوكيلات ON العملاء.[ID_client]=التوكيلات.clientID
WHERE (((التوكيلات.العميل)=Forms![العملاء_رئيسي]!العميل))
ORDER BY العملاء.[ID_client];
